let age = 16;

switch (age) {
  case 10:
    console.log("You are 10 years old");
    break;

  case 13:
    console.log("You are 13 years old");
    break;

  case 16:
    console.log("You are 16 years old");
    break;

  case 18:
    console.log("You are 18 years old");
    break;

  case 20:
    console.log("You are 20 years old");
    break;

  default:
    console.log("Your age is not listed");
} 




